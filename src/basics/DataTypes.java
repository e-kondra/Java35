package basics;

public class DataTypes {
    public static void main(String[] args) {
        //Integer Data types
        byte numOne = 100;
        short numTwo = 1000;
        int numThree = 345253;
        long numFour = 4123556221L;

        //Floating point
        float numFive = 123456.23655f;
        double numSix = 2123.23233655454d;

        //Boolean
        boolean redLightSwitch = true;
        boolean blackLightSwitch = false;

        //Character
        char charOne = '%';
        char charTwo = 'x';
        char charThree = '0';

        //String
        String textOne = "It's a good text";
        //print them out
        System.out.println(charThree);
        System.out.println(redLightSwitch);
        System.out.println(textOne);
        System.out.println(numSix);
    }
}
